# State names for dropdown
STATES = {
    'OK': 'Oklahoma',
    'TX': 'Texas', 
    'KS': 'Kansas',
    'AR': 'Arkansas',
    'MO': 'Missouri',
}

def get_states():
    """Return list of state abbreviations that have FIPS data"""
    return sorted(FIPS_CODES.keys())

def get_state_name(abbrev):
    """Get full state name from abbreviation"""
    return STATES.get(abbrev, abbrev)

def get_counties(state_abbrev):
    """Return sorted list of county names for a state"""
    if state_abbrev in FIPS_CODES:
        return sorted(FIPS_CODES[state_abbrev].keys())
    return []

def get_fips_code(state_abbrev, county_name):
    """Get FIPS code for a county"""
    if state_abbrev in FIPS_CODES:
        return FIPS_CODES[state_abbrev].get(county_name)
    return None

def get_county_from_fips(fips_code):
    """Reverse lookup - get state and county from FIPS code"""
    for state, counties in FIPS_CODES.items():
        for county, code in counties.items():
            if code == fips_code:
                return state, county
    return None, None


